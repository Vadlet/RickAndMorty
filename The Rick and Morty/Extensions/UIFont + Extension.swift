import UIKit

extension UIFont {
    
    static func avenirMedium13() -> UIFont? {
        return UIFont(name: "Avenir-Medium", size: 13)
    }
    
    static func avenirHeavy19() -> UIFont? {
        return UIFont(name: "Avenir-Heavy", size: 19)
    }
    
    static func avenirHeavy25() -> UIFont? {
        return UIFont(name: "Avenir-Heavy", size: 25)
    }
}
