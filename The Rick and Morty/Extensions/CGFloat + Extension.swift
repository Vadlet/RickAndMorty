import UIKit

// MARK: - Constraints Constant

extension CGFloat {
    
    static let smallOffset: CGFloat = 5
    static let offset: CGFloat = 16
    static let inset: CGFloat = -16
    static let smallInset: CGFloat = -5
    static let buttonHeight: CGFloat = 300
    static let buttonSpacing: CGFloat = 10
    static let heightCell: CGFloat = 110
    static let widthImage: CGFloat = 100
    static let smallSpacing: CGFloat = 2
    static let spacing: CGFloat = 6
    static let cornerRadius: CGFloat = 15
    static let withSplashIcon = 306.5
    static let heightSplashIcon = 318.5
    static let zero = 0
}

// MARK: - CGAffineTransform Size

extension CGAffineTransform {
    static let scaleX: CGFloat = 0.001
    static let scaleY: CGFloat = 0.001
}

// MARK: - Dispatch Time

extension DispatchTime {
    static let delay: CGFloat = 0.65
}

// MARK: - Time Interval

extension TimeInterval {
    static let delay65: CGFloat = 0.65
    static let delay45: CGFloat = 0.45
}
